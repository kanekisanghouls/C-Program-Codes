//================================= BANKING CODES #1 =================================
#include <stdio.h>

struct bank{
	int account_no;
	char name[50];
	float balance;
};

int main(){
	struct bank b1;

	printf("Enter Account Number: ");
	scanf("%d", &b1.account_no);
	printf("Enter Name: ");
	scanf("%s", b1.name);
	printf("Enter Balance: ");
	scanf("%f", &b1.balance);

	printf("\nAccount Number: %d\n", b1.account_no);
	printf("Name: %s\n", b1.name);
	printf("Balance: %f\n", b1.balance);

	return 0;
}

